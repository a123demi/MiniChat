package com.lming.minichat.bean;

public class ApplicationListBean {
	private int appImgId;
	private String appName;
	/**
	 * @return the appImgId
	 */
	public int getAppImgId() {
		return appImgId;
	}
	/**
	 * @param appImgId the appImgId to set
	 */
	public void setAppImgId(int appImgId) {
		this.appImgId = appImgId;
	}
	/**
	 * @return the appName
	 */
	public String getAppName() {
		return appName;
	}
	/**
	 * @param appName the appName to set
	 */
	public void setAppName(String appName) {
		this.appName = appName;
	}
}
